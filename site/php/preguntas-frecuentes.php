			  	<div class="panel-heading">
			    	<h3 class="panel-title">¿Qué seguros se contratan al adquirir un crédito hipotecario?</h3>
			  	</div>
			 	<div class="panel-body">
			 	<p>
			    	Dependiendo de la entidad financiera al adquirir un crédito hipotecario se contratan los siguientes seguros: </p>
			    	<ul >
  						<li >Seguro de vida e incapacidad total o permanente</li>
 						 <li >Seguro de daños para la propiedad en garantía</li>
 						 <li >Seguro de desempleo ( en caso de ser asalariado )</li>
 						 <li >Seguro de contenidos ( para algunas entidades financieras )</li>					 
 						 </ul>
			  	</div>
