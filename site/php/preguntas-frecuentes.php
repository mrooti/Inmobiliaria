			    	<h3 class="panel-title">¿Que es un crédito hipotecario bancario?</h3>
			  	</div>
			 	<div class="panel-body">
			 	<p>
			    	Un crédito hipotecario es un préstamo que se hace a largo plazo, el cual está respaldado en la hipoteca de la casa que se compra.</p>
			    	<p>
					Cuando acudes a una institución financiera y pides un crédito, solicitas una cierta cantidad de dinero que te comprometes a devolver pagando cierto interés adicional y otros gastos dentro de un plazo determinado.</p>
					</p>
					Al tratarse de la compra de un bien de un valor importante, la institución financiera exige entonces una garantía que le asegure el pago del dinero prestado. Dicha garantía es precisamente el terreno o la propiedad recién adquirida con el crédito, que pasa a estar hipotecada.</p>
			  	</div>
			</div>
			<div class="panel panel-default">
			  	<div class="panel-heading">
			    	<h3 class="panel-title">¿Qué plazos disponibles existen para pagar un crédito hipotecario?</h3>
			  	</div>
			 	<div class="panel-body">
			 	Los plazos para pagar un crédito hipotecario son variados pueden ir desde 5 hasta 30 años. Pero actualmente es posible liquidar anticipadamente los mismos sin pagar alguna penalización.	
			  	</div>
			</div>
			<div class="panel panel-default">
			  	<div class="panel-heading">
			    	<h3 class="panel-title">¿Que es un crédito hipotecario bancario?</h3>
			  	</div>
			 	<div class="panel-body">
			 	<p>
			    	Es importante considerar el factor de la edad del cliente, que puede definirnos el plazo máximo para contratar un crédito. </p>
			    	<p>
					La edad mínima que debe tener el cliente al momento de contratar un crédito hipotecario es de 25 años.</p>
					</p>
					Dependiendo de la institución financiera se pueden contratar créditos hipotecarios con una edad máxima que va desde los 64 años y 11 meses hasta los 80 años, sumando la edad actual del cliente más el plazo que se pretende contratar.</p>
			  	</div>
			</div>
