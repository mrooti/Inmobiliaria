			    	<h3 class="panel-title">¿Que son los créditos cofinanciados con Fovissste e Infonavit ?</h3>
			  	</div>
			 	<div class="panel-body">
			 	<p>
			    	Cuando los financiamientos otorgados por cualquiera de estos dos institutos de vivienda son insuficientes para adquirir la vivienda que deseamos, tenemos la opción de solicitar un cofinanciamiento con una entidad financiera. </p>
			    	<p>
					Un cofinanciamiento es un préstamo realizado por dos instituciones financieras para la compra de la misma casa o departamento. En México existen dos instituciones que manejan este esquema de financiamiento: El Instituto Nacional para la Vivienda de los Trabajadores (INFONAVIT) y el Fondo de la Vivienda de Instituto de Seguridad y Servicios Sociales de los Trabajadores del Estado (FOVISSSTE)</p>
					Ambas instituciones trabajan en colaboración con bancos, Sofoles (Sociedad Financiera de Objeto Limitado) y Sofomes (Sociedad Financiera de Objeto Múltiple).  El objetivo de estos créditos es el poder acceder a un préstamo mayor.</p>
					</p>
					Tipos de cofinamiento manejados por ambas instituciones.</p>
					<p>
					Cofinanciamiento Infonavit:</p>
					<ul >
  						<li >Cofinavit: Contar con 116 puntos o los bimestres de cotización continua que se establezcan en el producto de crédito que elijas.</li>
 						 <li >Cofinanciamiento ingresos adicionales: Cuando los ingresos son de $7,389.84 (De 1 a 3.9 VSM) más los ingresos externos que puedas comprobar.</li>					 
 						 </ul>
 					<p>
					Cofinanciamiento Fovissste:</p>
					<ul >
  						<li >Alia2: El monto del crédito se determina por los flujos futuros de las aportaciones patronales.</li>
 						 <li >Respalda2: El crédito otorgado estará en relación con el monto total ahorrado en la subcuenta de vivienda.</li>					 
 						 </ul>
 						 </p>
					En un cofinanciamiento, parte del valor de la casa es aportado por el instituto al que te encuentres afiliado (Infonavit o Fovissste) y la otra parte por la institución financiera.</p>
			  	</div>
