			    	<h3 class="panel-title">¿Qué es la tasa de interés?</h3>
			  	</div>
			 	<div class="panel-body">
			 	<p>En el ámbito de la economía y las finanzas, el concepto de interés hace referencia al costo que tiene un crédito. Se trata de un término que, por lo tanto, permite describir al provecho, utilidad, valor o la ganancia de una determinada cosa o actividad.</p>
			 	<p>En los créditos hipotecarios que se ofertan actualmente esta tasa de interés oscila entre 7.25 y el 11%. Va a depender de la institución bancaria, monto de vivienda, monto de crédito y plazo a contratar principalmente.</p>	
