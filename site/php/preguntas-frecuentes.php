			    	<h3 class="panel-title">¿Quiénes tienen acceso al Infonavit?</h3>
			  	</div>
			 	<div class="panel-body">
			 		<p>
			    	Si bien el Infonavit nació, y se mantuvo durante muchos años, orientado a llevar soluciones de vivienda a los trabajadores afiliados al Instituto Mexicano del Seguro Social (IMSS), en años recientes amplió su ámbito de acción enfocando sus esfuerzos también a los trabajadores no afiliados a este instituto.</p>
			    	<p>
					Al grupo de personas que tiene acceso a los productos y esquemas del Infonavit, por ser afiliados al IMSS, se le denomina Derechohabientes del Infonavit.</p>
					Los programas de Infonavit están enfocados a dos grupos de personas:</p>
					</p>
					Trabajadores afiliados al Instituto Mexicano del Seguro Social con una relación laboral vigente. En el caso de los derechohabientes, el patrón es  responsable de realizar cada mes aportaciones por el 5% del salario del trabajador a la subcuenta de vivienda contenida en la Administradora de Fondos para el Retiro (AFORE) que el trabajador haya seleccionado</p>
					<p>
					Trabajadores no derechohabientes. En este caso, las personas que trabajen  en labores domésticas, el patrón puede aportar una cuota mensual que les permite, en el mediano plazo, obtener un crédito para vivienda, o bien formar un ahorro para su retiro. Para los trabajadores que se encuentren en este esquema y deseen tener acceso a los programas del Infonavit se creó el programa "Infonavit para todos".</p>
